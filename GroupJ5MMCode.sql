--GENERATED BY DEFAULT AS IDENTITY will auto-increment the custID
CREATE TABLE customer (
custID		NUMBER(4) GENERATED BY DEFAULT AS IDENTITY,
custFtName	VARCHAR2(25),
custLtName	VARCHAR(25),
custPhNum	CHAR(12),
custAddr	VARCHAR2(100),
custPCode	VARCHAR2(7),
custEmail 	VARCHAR2(50) 	NOT NULL UNIQUE,
custRtnDate	DATE,
custAmtPaid 	NUMBER(3),
CONSTRAINT 	cust_pk 	PRIMARY KEY (custID)
);


CREATE TABLE rental_price (
boatClassSize	VARCHAR2(25) CONSTRAINT boatType PRIMARY KEY,	
rentFDPrice	NUMBER(5),
rentHDPrice	NUMBER(5),
rent2DPrice	NUMBER(5),

CONSTRAINT 	ck_boatType 	
CHECK (boatClassSize IN ('VLrg_SBoat', 'Lrg_SBoat', 'Std_SBoat', 'Sml_SBoat',
'VLrg_MBoat', 'Lrg_MBoat', 'Std_MBoat', 'Sml_MBoat',
'VLrg_RBoat', 'Lrg_RBoat', 'Std_RBoat', 'Sml_RBoat'))
);


CREATE TABLE manufacturer (
manuID		NUMBER(4),
manuName	VARCHAR2(50),
manuTele	CHAR(11),
manuAddr	VARCHAR2(50),
manuPCode	VARCHAR2(7),
manuEmail	VARCHAR2(50)	UNIQUE,

CONSTRAINT	manu_pk		PRIMARY KEY (manuID)
);


CREATE TABLE part (
partID		NUMBER(9),
partName    	VARCHAR2(50),
stock   	NUMBER(5),
delvDate	DATE,
delvNotes	VARCHAR2(100),
inUse		NUMBER(4),

CONSTRAINT 	parts_inUse_ck	CHECK (inUse <= stock),

CONSTRAINT 	part_pk		PRIMARY KEY (partID)
);


CREATE TABLE dealer (
dlrID		NUMBER(4),
dlrName		VARCHAR2(50),
dlrTele		CHAR(11),
dlrAddr		VARCHAR2(100),
dlrEmail	VARCHAR2(50)	UNIQUE,

CONSTRAINT	dlr_id_pk	PRIMARY KEY (dlrID)
);


CREATE TABLE boat (
boatID		NUMBER(5),
boatClassSize	VARCHAR2(25),
boatModel	VARCHAR2(25),
manuID		NUMBER(4)	NOT NULL,
boatBuyDate	DATE,
boatPrice	NUMBER(9),
boatLastServ 	DATE,
Sold 		VARCHAR2(1) 	DEFAULT 'N',
CONSTRAINT 	sold_bool CHECK (Sold IN ('Y', 'N')),

CONSTRAINT	boat_pk		PRIMARY KEY (boatID), 
  
CONSTRAINT	boat_boatType_fk FOREIGN KEY (boatClassSize)
REFERENCES	rental_price	(boatClassSize),

CONSTRAINT	manu_fk		FOREIGN KEY (manuID)
REFERENCES	manufacturer 	(manuID)
);


CREATE TABLE boatSold (
boatID       NUMBER(5),
dlrID        NUMBER(4),
saleDate     DATE,
salePrice    NUMBER(5),
CONSTRAINT boat_dlr_id_pk PRIMARY KEY (dlrID, boatID),

CONSTRAINT sold_boat_id_fk FOREIGN KEY (boatID)
    REFERENCES boat (boatID),
    
CONSTRAINT sold_dlr_id_fk FOREIGN KEY (dlrID)
    REFERENCES dealer (dlrID)
);


--Made changes to rent_reservation. I will explain.
CREATE TABLE rent_reservation (
rent_resNum        NUMBER(4),
boatClassSize  VARCHAR2(25),
custID		   NUMBER(4),
rentDate	   DATE,
rentPeriod     CHAR(2),
hirePrice      NUMBER(5),
paid           CHAR(1) DEFAULT NULL,

boatID		   NUMBER(5) DEFAULT NULL,
custName       VARCHAR(50) DEFAULT NULL,
dateDue        DATE DEFAULT NULL,
dateReturned   DATE DEFAULT NULL,
    
CONSTRAINT	rent_res_pk	PRIMARY KEY (rent_resNum),
CONSTRAINT	rent_boatType_fk FOREIGN KEY (boatClassSize)
REFERENCES	rental_price (boatClassSize),
CONSTRAINT	custID_fk	FOREIGN KEY (custID)
REFERENCES	customer	(custID),
CONSTRAINT	boat_rent_fk	FOREIGN KEY (boatID)
REFERENCES	boat		(boatID),

CONSTRAINT rent_res_period_ck CHECK (rentPeriod IN ('HD', 'FD', '2D')),
CONSTRAINT rent_res_paid_ck CHECK (paid = 'Y' OR paid = NULL)
);

--Create Sequence for Maintenance
CREATE SEQUENCE maint_refNum_boatID_seq
    INCREMENT BY 1
    START WITH 1
    MINVALUE 1;

--Set next value as DEFAULT, so there is no need to explicitly INSERT it
CREATE TABLE maintenance (
maintRefNum	NUMBER(9) DEFAULT maint_refNum_boatID_seq.NEXTVAL,
boatID		NUMBER(5)	NOT NULL,
maintFaultDet	VARCHAR2(50),
maintFaultDate	DATE,
maintActTaken	VARCHAR2(50),
maintActDate	DATE,
boatMaintPrio	NUMBER(1)	DEFAULT 2,
CONSTRAINT	maint_refNum_boatID_pk	PRIMARY KEY (maintRefNum, boatID),
CONSTRAINT	boat_maint_fk	FOREIGN KEY (boatID)
REFERENCES	boat		(boatID)
);


CREATE TABLE manufacturer_part (
manuID		NUMBER(4),
partID		NUMBER(9),
CONSTRAINT	manu_part_pk	PRIMARY KEY (manuID, partID),
CONSTRAINT	manu_part_manu_fk FOREIGN KEY (manuID)
REFERENCES	manufacturer	(manuID),
CONSTRAINT	manu_part_part_fk FOREIGN KEY (partID)
REFERENCES	part		(partID)
);

